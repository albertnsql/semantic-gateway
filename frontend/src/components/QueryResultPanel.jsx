/* ── Main export ── */
export default function QueryResultPanel({ response, error, defaultTab = 'Tabular Results', onSuggestionClick, isMostRecent = true }) {
  const [activeTab, setActiveTab] = useState(defaultTab);
  const [userExpanded, setUserExpanded] = useState(false);

  if (error) {
    return (
      <div
        className="flex items-start gap-4 p-6 rounded-[32px] backdrop-blur-xl animate-fade-in w-full"
        style={{ background: 'rgba(244,63,94,0.06)', boxShadow: '8px 8px 16px rgba(244,63,94,0.06), -6px -6px 12px rgba(255,255,255,0.9)' }}
      >
        <XCircle size={20} className="text-[#F43F5E] shrink-0 mt-0.5" />
        <div>
          <p className="text-[#F43F5E] font-bold font-dm text-base">
            {error.response?.data?.error
              ? `${error.response.data.error}: ${error.response.data.message}`
              : error.message || 'API request failed'}
          </p>
          {error.response?.data?.detail && (
            <pre className="mt-2 text-xs text-[#F43F5E]/70 font-mono whitespace-pre-wrap">
              {error.response.data.detail}
            </pre>
          )}
          {!error.response && (
            <p className="text-sm text-[#4A7B76] mt-1 font-dm">
              Could not reach the backend. Is the gateway running?
            </p>
          )}
        </div>
      </div>
    );
  }

  if (!response) return null;

  const { status, query, validation, governance, result, rejection } = response;
  const narrativeSummary = response.narrative_summary || '';
  const isRejected      = status === 'rejected';
  const isClarification = status === 'needs_clarification';
  const isSuccess       = status === 'success' || status === 'dry_run';
  const isSchema        = status === 'schema_response';
  const isOutOfScope    = status === 'out_of_scope';

  const isExpanded = isMostRecent || userExpanded;

  if (isClarification) return <ClarificationPanel response={response} />;
  if (isSchema) return <SchemaResponsePanel message={response.message} />;
  if (isOutOfScope) {
    return <OutOfScopePanel message={response.message} suggestedQuery={response.suggested_query} onSuggestionClick={onSuggestionClick} />;
  }

  // Handle collapsed state
  if (!isExpanded && isSuccess) {
    const firstSentence = narrativeSummary ? narrativeSummary.split('.')[0] + '.' : '';
    const metricText = query?.interpreted_metrics?.join(', ') || 'No metrics';
    const dimText = query?.interpreted_dimensions?.join(', ') || '';
    const byDimText = dimText ? `by ${dimText}` : '';
    
    return (
      <div 
        className="flex items-center gap-4 px-6 py-4 rounded-[24px] cursor-pointer hover:-translate-y-0.5 transition-all duration-200 backdrop-blur-xl w-full"
        style={{ background: 'rgba(255,255,255,0.65)', boxShadow: CLAY_SHADOW }}
        onClick={() => setUserExpanded(true)}
        title="Click to expand"
      >
        <div className="w-8 h-8 rounded-full flex items-center justify-center shrink-0 bg-emerald-50 shadow-inner">
          <CheckCircle2 size={16} className="text-emerald-500" />
        </div>
        <div className="flex items-center gap-3 text-sm text-[#1A3A38] font-dm truncate flex-1">
          <span className="font-bold text-[15px] truncate">{metricText}</span>
          {byDimText && <span className="text-neu-muted truncate">{byDimText}</span>}
          <span className="text-neu-muted px-3 py-1 bg-white/50 rounded-full shrink-0 shadow-sm">{result?.row_count ?? 0} rows</span>
          <span className="text-[#4A7B76] truncate ml-2 text-[13px]">{firstSentence}</span>
        </div>
        <ChevronRight size={18} className="text-[#0D9488] shrink-0" />
      </div>
    );
  }

  return (
    <div 
      className="flex flex-col backdrop-blur-xl rounded-[32px] overflow-hidden w-full animate-slide-in"
      style={{ background: 'rgba(255,255,255,0.7)', boxShadow: CLAY_SHADOW }}
    >
      {/* Premium Compact header row — success only */}
      {isSuccess && (
        <div 
          className="flex flex-wrap items-center gap-x-6 gap-y-3 px-8 py-5 text-sm font-dm"
          style={{ background: 'rgba(255,255,255,0.4)', borderBottom: '1px solid rgba(13,148,136,0.1)' }}
        >
          <div className="flex items-center gap-3 shrink-0">
            <CheckCircle2 size={18} className="text-emerald-500" />
            <StatusBadge status={status} label={status.replace('_', ' ')} />
          </div>
          
          <div className="flex items-center gap-3 border-l border-[#0D9488]/10 pl-6 truncate">
            <span className="text-[#4A7B76] text-xs uppercase font-bold tracking-wider shrink-0">Metrics</span>
            <span className="font-bold text-[#1A3A38] text-[15px] truncate">{query?.interpreted_metrics?.join(', ') || '—'}</span>
          </div>

          <div className="flex items-center gap-3 border-l border-[#0D9488]/10 pl-6 truncate">
            <span className="text-[#4A7B76] text-xs uppercase font-bold tracking-wider shrink-0">Dims</span>
            <span className="font-bold text-[#1A3A38] text-[15px] truncate">{query?.interpreted_dimensions?.join(', ') || '—'}</span>
          </div>

          <div className="flex items-center gap-3 border-l border-[#0D9488]/10 pl-6 shrink-0">
            <span className="text-[#4A7B76] text-xs uppercase font-bold tracking-wider">Rows</span>
            <span className="font-bold text-[#1A3A38] text-[15px] bg-white/60 px-2.5 py-0.5 rounded-full shadow-sm">{result?.row_count ?? 0}</span>
          </div>

          <div className="flex items-center gap-3 border-l border-[#0D9488]/10 pl-6 truncate">
            <span className="text-[#4A7B76] text-xs uppercase font-bold tracking-wider shrink-0">Time</span>
            <span className="font-bold text-[#1A3A38] text-[15px] truncate">{query?.time_range ? `${query.time_range.start_date} → ${query.time_range.end_date}` : '—'}</span>
          </div>

          {response.cache_hit === true && (
            <div className="ml-auto shrink-0 pl-2">
              <span className="inline-flex items-center gap-1.5 px-3 py-1 rounded-[8px] text-[11px] font-bold text-[#0D9488] bg-teal-50 border border-teal-100" title="This result was served from cache">
                <Database size={12} /> Cached
              </span>
            </div>
          )}
        </div>
      )}

      {/* Rejection detail */}
      {isRejected && (
        <div className="p-8 border-b border-[#D1E2DE]/50">
          <RejectionPanel rejection={rejection} />
        </div>
      )}

      {/* Tabbed panels — success */}
      {isSuccess && (
        <div className="flex flex-col">
          <div className="px-8 pt-6 pb-0">
            <Tabs
              tabs={['Summary', 'Tabular Results', 'Governance', 'SQL', 'Lineage']}
              active={activeTab}
              onSelect={setActiveTab}
            />
          </div>
          <div className="p-8 pt-6">
            {activeTab === 'Summary'    && <NarrativeSummary text={narrativeSummary} query={query?.original_query || ''} lineagePath={governance?.lineage_path} />}
            {activeTab === 'Tabular Results'    && <ResultsTable data={result?.data} />}
            {activeTab === 'Governance' && <GovernancePanel governance={governance} validation={validation} />}
            {activeTab === 'SQL'        && (
              <SqlViewer
                generatedSql={result?.generated_sql}
                metricflowQuery={result?.metricflow_query}
              />
            )}
            {activeTab === 'Lineage'   && (
              <div className="flex flex-col gap-6">
                <LineageGraph path={governance?.lineage_path} steps={[]} />
                {governance?.lineage_path?.length > 0 && (
                  <div className="overflow-x-auto rounded-[20px]" style={{ boxShadow: CLAY_INSET }}>
                    <table className="w-full text-sm font-dm">
                      <thead>
                        <tr className="bg-[rgba(255,255,255,0.4)] border-b border-[#D1E2DE]/50">
                          <th className="px-4 py-3 text-left text-xs font-mono text-neu-muted uppercase tracking-wide">Step</th>
                          <th className="px-4 py-3 text-left text-xs font-mono text-neu-muted uppercase tracking-wide">Model</th>
                          <th className="px-4 py-3 text-left text-xs font-mono text-neu-muted uppercase tracking-wide">Layer</th>
                        </tr>
                      </thead>
                      <tbody className="divide-y divide-[#D1E2DE]/40">
                        {governance.lineage_path.map((m, i) => {
                          const isLast = i === governance.lineage_path.length - 1;
                          return (
                            <tr key={i} className="hover:bg-[rgba(255,255,255,0.5)] transition-colors">
                              <td className="px-4 py-3 text-neu-muted font-mono text-xs">{i + 1}</td>
                              <td className="px-4 py-3">
                                <code className={`text-[13px] font-mono ${isLast ? 'text-[#0D9488] font-bold' : 'text-[#1A3A38]'}`}>{m}</code>
                              </td>
                              <td className="px-4 py-3">
                                <StatusBadge
                                  status={isLast ? 'certified' : 'info'}
                                  label={isLast ? 'metric' : m.startsWith('stg_') ? 'staging' : m.startsWith('int_') ? 'intermediate' : m.startsWith('fct_') || m.startsWith('dim_') ? 'marts' : 'raw'}
                                />
                              </td>
                            </tr>
                          );
                        })}
                      </tbody>
                    </table>
                  </div>
                )}
              </div>
            )}
          </div>
        </div>
      )}

      {response?.request_id && (
        <div className="px-8 py-3 bg-[rgba(255,255,255,0.3)] border-t border-[#0D9488]/10 flex justify-between items-center">
          <p className="text-[10px] text-[#4A7B76] font-mono opacity-60">request_id: {response.request_id}</p>
        </div>
      )}
    </div>
  );
}
