/* ── Results table ── */
function ResultsTable({ data }) {
  const [showAll, setShowAll] = useState(false);

  if (!data || data.length === 0) {
    return (
      <p className="text-neu-muted text-sm font-dm text-center py-8">
        No data rows returned (dry run or empty result set).
      </p>
    );
  }
  const cols      = Object.keys(data[0]);
  const displayed = showAll ? data : data.slice(0, 20);

  return (
    <div className="overflow-x-auto rounded-[20px]" style={{ boxShadow: CLAY_INSET }}>
      <table className="w-full text-sm font-dm">
        <thead>
          <tr className="bg-[rgba(255,255,255,0.4)] border-b border-[#D1E2DE]/50">
            {cols.map((c) => (
              <th key={c} className="px-4 py-3 text-left text-xs font-mono text-neu-muted uppercase tracking-wide whitespace-nowrap">
                {c}
              </th>
            ))}
          </tr>
        </thead>
        <tbody className="divide-y divide-[#D1E2DE]/40">
          {displayed.map((row, i) => (
            <tr key={i} className="hover:bg-[rgba(255,255,255,0.5)] transition-colors">
              {cols.map((c) => {
                let displayValue = <span className="text-neu-muted/40">null</span>;
                if (row[c] != null) {
                  const val = row[c];
                  const cLower = c.toLowerCase();
                  if (typeof val === 'number' && (cLower.endsWith('_rate') || cLower.endsWith('_pct') || cLower.endsWith('_ctr'))) {
                    displayValue = `${(val * 100).toFixed(2)}%`;
                  } else if (typeof val === 'number' && !Number.isInteger(val)) {
                    displayValue = val.toFixed(2);
                  } else {
                    displayValue = String(val);
                  }
                }
                return (
                  <td key={c} className="px-4 py-3 text-neu-primary whitespace-nowrap font-dm">
                    {displayValue}
                  </td>
                );
              })}
            </tr>
          ))}
        </tbody>
      </table>
      {data.length > 20 && !showAll && (
        <div className="flex justify-center p-3 border-t border-[#D1E2DE]/40 bg-[rgba(255,255,255,0.2)]">
            <button 
                onClick={() => setShowAll(true)}
                className="text-xs font-bold text-[#0D9488] hover:text-[#0F766E] transition-colors"
            >
                Show all {data.length} rows
            </button>
        </div>
      )}
      {data.length > 20 && showAll && (
        <div className="flex justify-center p-3 border-t border-[#D1E2DE]/40 bg-[rgba(255,255,255,0.2)]">
            <button 
                onClick={() => setShowAll(false)}
                className="text-xs font-bold text-[#0D9488] hover:text-[#0F766E] transition-colors"
            >
                Show fewer rows
            </button>
        </div>
      )}
    </div>
  );
}

